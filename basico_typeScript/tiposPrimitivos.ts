
// Em TypeScript, assim como em JavaScript, existem cinco tipos primitivos:

// number: Representa números, tanto inteiros quanto em ponto flutuante.
// Exemplo de uso:
let idade: number = 25;
let preco: number = 10.99;

// string: Representa sequências de caracteres.
// Exemplo de uso:
let nome: string = "João";
let mensagem: string = `Olá, ${nome}!`;


// boolean: Representa valores lógicos verdadeiro ou falso.
// Exemplo de uso:
let ativo: boolean = true;
let desligado: boolean = false;


// null: Representa a ausência de valor intencional.
// Exemplo de uso:
let valor: number | null = null;


// undefined: Representa um valor não atribuído.
// Exemplo de uso:
let endereco: string | undefined;